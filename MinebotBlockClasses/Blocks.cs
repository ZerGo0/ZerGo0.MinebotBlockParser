    public readonly struct Block
    {
        public Block(ushort[] ids, ushort[] metadatas)
        {
            IDs = ids;
            Metadatas = metadatas;
        }

        //public readonly ushort ID;
        public readonly ushort[] IDs;
        public readonly ushort[] Metadatas;

        //TODO: Check if this is actually correct
        public override string ToString()
        {
            var idString = "";
            var firstId = false;
            foreach (var id in IDs)
            {
                foreach (var metadata in Metadatas)
                {
                    if (!firstId)
                    {
                        idString += id + (metadata != 0 ? ":" + metadata : "");
                        firstId = true;
                    }
                    else idString += "," + id + (metadata != 0 ? ":" + metadata : "");
                }
            }

            return idString;
        }
    }

